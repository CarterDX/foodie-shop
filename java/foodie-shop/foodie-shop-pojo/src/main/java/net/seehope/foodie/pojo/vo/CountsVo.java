package net.seehope.foodie.pojo.vo;

public class CountsVo {
	private String totalCounts;
	private String goodCounts;
	private String normalCounts;
	private String badCounts;

	public String getTotalCounts() {
		return totalCounts;
	}

	public void setTotalCounts(String totalCounts) {
		this.totalCounts = totalCounts;
	}

	public String getGoodCounts() {
		return goodCounts;
	}

	public void setGoodCounts(String goodCounts) {
		this.goodCounts = goodCounts;
	}

	public String getNormalCounts() {
		return normalCounts;
	}

	public void setNormalCounts(String normalCounts) {
		this.normalCounts = normalCounts;
	}

	public String getBadCounts() {
		return badCounts;
	}

	public void setBadCounts(String badCounts) {
		this.badCounts = badCounts;
	}

}
